LI R1 0x0001 		;将R1寄存器赋值为1
LI R2 0x0001 		;将R2寄存器赋值为1
LI R3 0x0085 		;将R3寄存器赋值为80h
SLL R3 R3 0x0000 	;R3逻辑左移8位，变为8000h
LI R4 0x0005 		;将R4寄存器赋值为9，规定循环次数为9
SW R3 R1 0x0000 	;将R1的值写入[R3+0]内存处
SW R3 R2 0x0001 	;将R2的值写入[R3+1]内存处
ADDU R1 R2 R1 		;R1=R1+R2
ADDU R1 R2 R2 		;R2=R1+R2
ADDIU R3 0x0002 	;R3=R3+2
ADDIU R4 0x00FF 	;R4=R4-1
BNEZ R4 0x00F9 		;跳转到指令（SW R3 R1 0）处